/** \brief realiza la suma de 2 numeros enteros
 *
 * \param primer numero a sumar.
 * \param segundo numero a sumar.
 * \return int la suma de los enteros.
 *
 */
int sumar(int a, int b);






   /** \brief realiza la resta de dos numeros enteros.
    *
    * \param primer numero a restar.
    * \param segundo numero a restar.
    * \return el resultado de la resta de los enteros.
    *
    */
int restar(int a, int b);







/** \brief multiplica dos numeros enteros.
 *
 * \param primer numero a multiplicar.
 * \param segundo numero a multiplicar.
 * \return el resultado de la multiplicacion de los numeros enteros.
 *
 */
int multiplicar(int a, int b);







/** \brief divide 2 numeros flotantes positivos.
 *
 * \param numero que se va a dividir.
 * \param numero que divide al parametro anterior
 * \return el resultado de la division de dos numeros positivos.
 *
 */
float dividir(float a, float b);






/** \brief saca el factorial del numero ingresado.
 *
 * \param numero al cual le quieres sacar el factorial.
 * \return el resultado del numero factorizado.
 *
 */
int factorial(int a);






/** \brief menu que se muestra en pantalla que elije las operaciones a realizar.
 *
 * \return el numero elegido en el menu.
 *
 */
int menuDeOpciones();
